    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        String fname, lname;
        fname = request.getParameter("FIRSTNAME");
        lname = request.getParameter("LASTNAME");
        Student stu = new Student();
        try {
            stu.createStudent(fname, lname);
            String str = "You have successfully registered. Your ID is: "+stu.getStudentId()+". Use to log-in.";
            request.setAttribute("register", str);
        } catch (Exception ex) {
            Logger.getLogger(StudentRegisterServlet.class.getName()).log(Level.SEVERE, null, ex);
        }
        RequestDispatcher view = request.getRequestDispatcher("/login.jsp");
        view.forward(request, response);
    }

